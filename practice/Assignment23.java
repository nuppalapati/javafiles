package com.udemy.java;
//Write a program using while loop to print Even numbers from 1 to 100.
public class Assignment23 {
	public static void main(String[] args) {
		
	
	int start = 1;
	int end = 100 ;
	while(start <= end) {
		if(start%2==0) {
			System.out.println(start);
		}
		start++;
	}
	}}
	
	